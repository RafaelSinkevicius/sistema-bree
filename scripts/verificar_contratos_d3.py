def calcular_d3_correto(contrato, hoje):
    """
    Calcula a lógica D+3 correta conforme implementada em automacao.py
    """
    dia_vencimento = contrato.data_vigencia.day
    
    try:
        vencimento_mes_atual = contrato.data_vigencia.replace(year=hoje.year, month=hoje.month)
    except ValueError:
        if hoje.month == 2:
            vencimento_mes_atual = date(hoje.year, 2, 28)
        elif hoje.month in [4, 6, 9, 11] and dia_vencimento == 31:
            vencimento_mes_atual = date(hoje.year, hoje.month, 30)
        else:
            vencimento_mes_atual = date(hoje.year, hoje.month, dia_vencimento)
    
    data_para_checar = vencimento_mes_atual + timedelta(days=3)
    
    # CORREÇÃO D+3: Se D+3 cai no próximo mês, calcula quantos dias já se passaram
    if data_para_checar.month != hoje.month:
        dias_desde_vencimento = (hoje - vencimento_mes_atual).days
        pode_checar = dias_desde_vencimento >= 3
    else:
        pode_checar = hoje >= data_para_checar
    
    return {
        'vencimento_mes_atual': vencimento_mes_atual,
        'data_para_checar': data_para_checar,
        'dias_desde_vencimento': (hoje - vencimento_mes_atual).days if data_para_checar.month != hoje.month else None,
        'pode_checar': pode_checar,
        'd3_cai_proximo_mes': data_para_checar.month != hoje.month
    }


def verificar_contratos():
    """
    Verifica os contratos afetados e gera relatório
    """
    with app.app_context():
        fuso_brasilia = pytz.timezone("America/Sao_Paulo")
        hoje = datetime.now(fuso_brasilia).date()
        
        resultados = []
        contratos_nao_encontrados = []
        contratos_encontrados = []
        
        print(f"\n{'='*80}")
        print(f"VERIFICAÇÃO DE CONTRATOS AFETADOS PELA LÓGICA INCORRETA DO D+3")
        print(f"Data de verificação: {hoje}")
        print(f"Total de contratos na lista: {len(contratos_afetados)}")
        print(f"{'='*80}\n")
        
        for num_contrato in contratos_afetados:
            contrato = Contrato.query.filter_by(contrato=num_contrato).first()
            
            if not contrato:
                contratos_nao_encontrados.append(num_contrato)
                continue
            
            contratos_encontrados.append(num_contrato)
            
            # Calcula a lógica D+3 correta
            calc_d3 = calcular_d3_correto(contrato, hoje)
            
            # Determina se deveria ser checado hoje
            deve_checar_hoje = False
            motivo = ""
            
            if contrato.status == "Em atraso":
                deve_checar_hoje = contrato.data_checagem != hoje
                motivo = "Em atraso - checagem diária" if deve_checar_hoje else "Já checado hoje"
            elif contrato.status in ["Em dia", "Pago"]:
                if not contrato.verificado and calc_d3['pode_checar']:
                    deve_checar_hoje = True
                    motivo = "Em dia/Pago - D+3 atingido e não verificado"
                elif contrato.verificado:
                    motivo = "Já verificado neste mês"
                elif not calc_d3['pode_checar']:
                    motivo = f"D+3 ainda não atingido (dias desde vencimento: {calc_d3.get('dias_desde_vencimento', 'N/A')})"
            else:
                motivo = f"Status: {contrato.status}"
            
            resultado = {
                'contrato': num_contrato,
                'status': contrato.status,
                'data_vigencia': contrato.data_vigencia,
                'data_checagem': contrato.data_checagem,
                'verificado': contrato.verificado,
                'dias_atraso': contrato.dias_atraso,
                'parcela_atual': contrato.parcela_atual,
                'valor_parcela': float(contrato.valor_parcela) if contrato.valor_parcela else None,
                'vencimento_mes_atual': calc_d3['vencimento_mes_atual'],
                'data_para_checar': calc_d3['data_para_checar'],
                'dias_desde_vencimento': calc_d3.get('dias_desde_vencimento'),
                'd3_cai_proximo_mes': calc_d3['d3_cai_proximo_mes'],
                'pode_checar': calc_d3['pode_checar'],
                'deve_checar_hoje': deve_checar_hoje,
                'motivo': motivo
            }
            
            resultados.append(resultado)
        
        # Estatísticas
        total_encontrados = len(contratos_encontrados)
        total_nao_encontrados = len(contratos_nao_encontrados)
        
        # Agrupa por status
        por_status = {}
        por_deve_checar = {'sim': 0, 'nao': 0}
        
        for r in resultados:
            status = r['status']
            if status not in por_status:
                por_status[status] = 0
            por_status[status] += 1
            
            if r['deve_checar_hoje']:
                por_deve_checar['sim'] += 1
            else:
                por_deve_checar['nao'] += 1
        
        # Relatório no console
        print(f"\n{'='*80}")
        print("RESUMO DA VERIFICAÇÃO")
        print(f"{'='*80}")
        print(f"Contratos encontrados no banco: {total_encontrados}")
        print(f"Contratos não encontrados: {total_nao_encontrados}")
        print(f"\nDistribuição por status:")
        for status, count in sorted(por_status.items()):
            print(f"  {status}: {count}")
        print(f"\nContratos que DEVEM ser checados hoje: {por_deve_checar['sim']}")
        print(f"Contratos que NÃO devem ser checados hoje: {por_deve_checar['nao']}")
        print(f"{'='*80}\n")
        
        # Gera CSV com detalhes
        csv_filename = f"verificacao_contratos_d3_{hoje.strftime('%Y%m%d')}.csv"
        with open(csv_filename, 'w', newline='', encoding='utf-8') as csvfile:
            fieldnames = [
                'contrato', 'status', 'data_vigencia', 'data_checagem', 'verificado',
                'dias_atraso', 'parcela_atual', 'valor_parcela',
                'vencimento_mes_atual', 'data_para_checar', 'dias_desde_vencimento',
                'd3_cai_proximo_mes', 'pode_checar', 'deve_checar_hoje', 'motivo'
            ]
            writer = csv.DictWriter(csvfile, fieldnames=fieldnames)
            writer.writeheader()
            
            for r in resultados:
                row = r.copy()
                # Converte datas para string
                for key in ['data_vigencia', 'data_checagem', 'vencimento_mes_atual', 'data_para_checar']:
                    if row[key]:
                        row[key] = row[key].strftime('%Y-%m-%d')
                writer.writerow(row)
        
        print(f"Relatório detalhado salvo em: {csv_filename}")
        
        # Lista contratos não encontrados se houver
        if contratos_nao_encontrados:
            print(f"\n{'='*80}")
            print(f"CONTRATOS NÃO ENCONTRADOS NO BANCO ({len(contratos_nao_encontrados)}):")
            print(f"{'='*80}")
            for num in contratos_nao_encontrados[:50]:  # Mostra apenas os primeiros 50
                print(f"  {num}")
            if len(contratos_nao_encontrados) > 50:
                print(f"  ... e mais {len(contratos_nao_encontrados) - 50} contratos")
        
        # Lista contratos que devem ser checados hoje
        contratos_para_checar = [r for r in resultados if r['deve_checar_hoje']]
        if contratos_para_checar:
            print(f"\n{'='*80}")
            print(f"CONTRATOS QUE DEVEM SER CHECADOS HOJE ({len(contratos_para_checar)}):")
            print(f"{'='*80}")
            for r in contratos_para_checar[:20]:  # Mostra apenas os primeiros 20
                print(f"  {r['contrato']} - {r['status']} - {r['motivo']}")
            if len(contratos_para_checar) > 20:
                print(f"  ... e mais {len(contratos_para_checar) - 20} contratos")
        
        return resultados, contratos_nao_encontrados


